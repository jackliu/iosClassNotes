void demo1()
{
    //*  表示指向  &表示出某块内存的地址
    //[] 表示编译器，分配连续的内存空间
    //定义一个一维数组
    int arr[] ={1,2,3,4,5};
    //如何从数组中取值？
    //1 取单个值,通过下标索引，实际是内存单元格的编号
    //[0] 在连续的内存空间中[]，找到排第一个(0)的内存
    int a = arr[0];
    
    printf("数组的名字 %d \n",arr);
    printf("数组第一个元素的地址 %d \n",&arr[0]);
    //数组的名字 1606416528
    //数组第一个元素的地址 1606416528
    //arr 实际上就是数组在整个内存中的  起始地址
    //即，只要我们知道数组的名称，就知道数组在整个内存中那一块
    
    //分析数组与指针的关系
    int *p; //定义一个指向数组的指针
    //把数组的第一个元素的地址，赋给指针
    p = arr;
    //用指针从数组中取值
    printf("数组第一个位置的值: %d \n",*p);
    printf("数组第二个位置的值: %d \n",*(p+1));
    printf("数组第三个位置的值: %d \n",*(p+2));
    printf("数组第四个位置的值: %d \n",*(p+3));
    printf("数组第五个位置的值: %d \n",*(p+4));
    
}

