void demo3()
{
    //定义一个二维数组
    int arr[3][5] ={
        {78,77,89,79,90},
        {86,78,54,85,88},
        {88,98,72,66,91}
    };
    //取单个数据
    printf("第二门课程，第三个人的成绩 %d \n",arr[1][2]);
    //第二门课程，第三个人的成绩 54
    
    //打印所有课程，所有人的成绩
    for (int i=0; i<3; i++) {
        for (int j=0; j<5; j++) {
            printf("第 %d 门课程，第 %d 个人的成绩是 %d \n",(i+1),(j+1),arr[i][j]);
        }
    }
    
    
}


void demo4()
{
    //定义一个二维数组，连续的内存空间
    int arr[3][5] ={
        78,77,89,79,90,
        86,78,54,85,88,
        88,98,72,66,91
    };
    //得到数组指针
    int *p;
    p=arr;
    printf("------使用指针得到二维数组中的数据------  \n");
    
    //取单个数据
    printf("第二门课程，第三个人的成绩是 %d \n",*(p+7));
    
    //打印所有课程，所有人的成绩
    for (int i=0; i<15; i++) {
        *(p+i)=*(p+i)-10;  //把每个人的成绩减 10 分
        printf("每门课程成绩 %d \n",*(p+i));
    }
}






